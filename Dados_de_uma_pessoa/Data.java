package Dados_de_uma_pessoa;

class Data {

    private int dia;
    private int mes;
    private int ano;

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getdia() {
        return dia;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getmes() {
        return mes;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getano() {
        return ano;
    }

}