package Prova_1estagio;

public class Estudante {

    private String nome;
    private int idade;
    private String sexo;
    private String cpf;
    private Ut ut1;
    private Ut ut2;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public Ut getUt1() {
        return ut1;
    }

    public void setUt1(Ut ut1) {
        this.ut1 = ut1;
    }

    public Ut getUt2() {
        return ut2;
    }

    public void setUt2(Ut ut2) {
        this.ut2 = ut2;
    }

}
